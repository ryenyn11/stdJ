package s5for;

public class s1for {

	public static void main(String[] args) {
		/*
		 for(khởi tạo; điều kiện; bước lặp){
		   khối lệnh;
		   }
		 */
		for(int i=2; i<=12; i+=2) {
			System.out.println("i= "+i);
		}// i chạy từ 2 tới 12, mỗi lần tăng i 2 đơ vi cho tới khi i<=12 thì kết thúc

	}

}
